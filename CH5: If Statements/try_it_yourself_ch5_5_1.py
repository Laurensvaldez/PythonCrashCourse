# conditional tests

car = 'subaru'

print ("Is car == 'subaru'? I predict True")
print (car == 'subaru')

print("\nIs car == 'audi'? I predict False")
print (car == 'audi')

print ("True-tests")
person_interest = 'elba'
print (person_interest == 'elba')

mother = 'ruth'
print (mother == 'ruth')

father = 'rafael'
print (father == 'rafael')

brother = 'jonathan'
print (brother == 'jonathan')

aunt = 'crusita'
print(aunt == 'crusita')

print ('\nFalse-tests')

print (person_interest == 'paulien')
print (mother == 'crusita')
print (father == 'juan-felix')
print (brother == 'tatjana')
print (aunt == 'shakira')